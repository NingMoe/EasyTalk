$(function(){
	$("#pccomm-up").click(function(){
		if($("#pccom-area").val()==''){
			alert("请先填写意见");
		}else{
			$.ajax({
				type:'post',
				url:'personAdvice',
				data:{content:$("#pccom-area").val()},
				success:function(data){
					if (data==0) {
						alert("提交失败：请重新提交");
					}else{
						window.location.reload();
						alert("提交成功：感谢您宝贵的意见");
					}
				},
			})
		}
	})
})